#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	char	*n;
	char	*m;

	n = str1;
	m = (char *)str2;
	while (*n)
		n++;
	while (*m)
		*n++ = *m++;
	*n = '\0';
	return (str1);
}
