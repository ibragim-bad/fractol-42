#include "libft.h"
#include <stdio.h>

int		ft_wordsnb(char const *s, char c)
{
	size_t				nb;

	nb = 0;
	while (*s && *s == c)
		s++;
	if (*s)
		nb = 1;
	while (*s)
	{
		if (*s == c && *(s + 1) && *(s + 1) != c)
			nb++;
		s++;
	}
	return (nb);
}
